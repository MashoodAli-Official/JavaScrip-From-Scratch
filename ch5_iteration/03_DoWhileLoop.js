// In JavaScript, a do...while loop is a control flow statement that executes a 
// block of code at least once, and then repeats the execution as long as a 
// specified condition evaluates to true. 

var i = 0;
do {
    console.log('This is Do While Loop');
    i++;
} while (i < 5);

