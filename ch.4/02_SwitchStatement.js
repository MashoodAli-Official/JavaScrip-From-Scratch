//The switch case statement in JavaScript is also used for decision-making purposes. 
// In some cases, using the switch case statement is seen to be more convenient than if-else statements.
var num = 2;
switch (num) {
    case 0 : 
        console.log('Number is equal to zero ');
        break;
    case 2 :
        console.log("Number is equal to 2");
        break;
    case 3 :
        console.log("Number is equal to 3");
        break;
    default:
        console.log("Number is equal to 10 or greater than 10"); 
};
// output => Number is equal to 2